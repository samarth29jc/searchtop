// Reverse lookup from 3-letter codes
export const alpha3Codes = Object.fromEntries(
  Object.entries(countryCodes).map(([alpha2, data]) => [
    data.alpha3, 
    { 
      name: data.name, 
      alpha2: alpha2, 
      key: data.key 
    }
  ])
);

// Add this at the bottom of countryCodes.js
export function getCountryData(input) {
  if (!input) return null;
  
  const inputStr = String(input).toUpperCase();
  
  // Check 2-letter codes
  if (inputStr.length === 2 && countryCodes[inputStr]) {
    return countryCodes[inputStr];
  }
  
  // Check 3-letter codes
  if (inputStr.length === 3 && alpha3Codes[inputStr]) {
    return alpha3Codes[inputStr];
  }
  
  // Check country names or keys
  const lowerInput = String(input).toLowerCase().replace(/\s+/g, "");
  return Object.values(countryCodes).find(
    country => country.name.toLowerCase() === input.toLowerCase() || 
               country.key === lowerInput
  ) || null;
}